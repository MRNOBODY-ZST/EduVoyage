type MessageKey = keyof (typeof messages)['zh-CN']

export function useI18n() {
  const settings = useSettingsStore()
  const locale = computed(() => settings.locale as Locale)

  function t(key: MessageKey | string, fallback?: string, params?: Record<string, string | number>) {
    const table = messages[locale.value] || messages['zh-CN']
    const zhTable = messages['zh-CN']
    const template = (table as Record<string, string>)[key] || (zhTable as Record<string, string>)[key] || fallback || key
    if (!params) {
      return template
    }
    return template.replace(/\{(\w+)\}/g, (_, name: string) => String(params[name] ?? `{${name}}`))
  }

  return {
    locale,
    localeOptions,
    t,
  }
}
